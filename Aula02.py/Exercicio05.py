#Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média.

nota1 = float(input('Digite a primeira nota: ')) # digite a primeira nota -> variavel nota do aluno
nota2 = float(input('Digite a segunda nota: ')) # digite a segunda nota -> variavel nota do aluno

media = (nota1 + nota2) / 2 # calculo da media
print(f'Média = {media}') # mostra a media do aluno

