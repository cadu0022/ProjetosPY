#Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.

num = int(input('Digite um número: ')) # digite um número inteiro -> variavel num
print(f'Num = {num}\nSucessor = {num + 1}\nAntecessor = {num - 1}') # mostra na tela o número, seu sucessor e seu antecessor.

